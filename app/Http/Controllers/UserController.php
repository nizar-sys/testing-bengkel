     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {   
        $user = User::paginate(5)->withQueryString();

        if(request('search')) {
            $user->where('username', 'like', '%' . request('search') . '%');
        }

        return view('dashboard.datauser', [
            'users' => $user
        ]);
    }

